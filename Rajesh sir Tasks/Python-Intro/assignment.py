# # # # # # 6. Concept of PVM
# # # # # # PVM stands for Python Virtual Machine. It is the component of the Python interpreter that executes the bytecode generated from Python source code. The process involves these steps:

# # # # # Source Code: Python code (.py file) is written by the programmer.
# # # # # Compilation: The Python code is compiled into bytecode (.pyc file).
# # # # # Execution: PVM executes the bytecode using its runtime environment.
# # # # # PVM is responsible for interpreting the bytecode and ensuring the program runs as expected.
# # # # # #

# # # # # 7. Compiler vs Interpreter
# # # # #
# # # # Feature:              	Compiler	                                                                     Interpreter
# # # # Execution:	Translates entire source code at once into machine code (executable file).	Translates and executes source code line by line.
# # # # Speed:	Faster during execution since translation is done in advance.	Slower as translation and execution occur simultaneously.
# # # # Error Detection:	Detects errors after scanning the entire code.	Stops execution and reports errors when encountered.
# # # # Output:	Produces an executable file (.exe or machine code).	No separate file; executes the code directly.
# # # # Languages	Used by: languages like C, C++, and Java.	Used by languages like Python, JavaScript, Ruby.

# # # 8. Flavours of Python
# # # # # # Python has several flavours or implementations, each with its own strengths and use cases:
# # # # CPython: The default and most widely used implementation of Python, written in C. It is the standard version that most people refer to when they talk about Python.

# # # Jython: A Python implementation written in Java, allowing Python code to run on the Java platform and use Java libraries.

# # # IronPython: Python implemented in C# for use with the .NET framework. It allows Python programs to interact with .NET libraries.

# # # PyPy: A fast, alternative implementation of Python with a Just-In-Time (JIT) compiler, which aims to improve performance.

# # # MicroPython: A lean and efficient Python implementation designed for microcontrollers and small computing platforms like embedded systems.

# # # Stackless Python: A version of CPython with added support for micro-threads, which makes it efficient for concurrent programming.

